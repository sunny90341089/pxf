# JAVA_OPTS are used during START/STOP/RUN

AGENT_PATHS=""
JAVA_AGENTS=""
JAVA_LIBRARY_PATH=""

# DO NOT EDIT VALUES FOR THE VARIABLES BELOW -- they are generated by the start script
PXF_OPTS=(-Dlogging.config=${PXF_CONF}/conf/pxf-log4j2.xml)
if [ "$PXF_OOM_KILL" = true ]; then
  PXF_OPTS+=(-XX:OnOutOfMemoryError="${PXF_HOME}/bin/kill-pxf.sh %p")
fi
if [ -n "$PXF_OOM_DUMP_PATH" ]; then
  PXF_OPTS+=(-XX:+HeapDumpOnOutOfMemoryError -XX:HeapDumpPath=$PXF_OOM_DUMP_PATH)
fi
JAVA_OPTS=($PXF_JVM_OPTS $AGENT_PATHS $JAVA_AGENTS $JAVA_LIBRARY_PATH "${PXF_OPTS[@]}")
